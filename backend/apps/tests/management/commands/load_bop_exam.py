class Command(BaseCommand):
    help = 'Load the BOP exam test (Противовыбросовое оборудование и противофонтанная безопасность на ОПО) with 40 questions.'

    def handle(self, *args, **options):
        category, cat_created = Category.objects.get_or_create(
            name=CATEGORY_NAME,
            defaults={
                'name_kz': '',
                'name_en': 'Blowout prevention equipment',
                'description': '',
                'icon': '',
                'order': 0,
                'is_active': True,
            },
        )
        if cat_created:
            self.stdout.write(self.style.SUCCESS(f'Created category: {CATEGORY_NAME}'))
        else:
            self.stdout.write(f'Category already exists: {CATEGORY_NAME}')

        test, test_created = Test.objects.get_or_create(
            title=TEST_TITLE,
            defaults={
                'category': category,
                'language': 'ru',
                'is_standalone': True,
                'is_active': True,
                'passing_score': 80,
                'max_attempts': 3,
                'time_limit': 60,
                'requires_video_recording': False,
            },
        )
        if test_created:
            self.stdout.write(self.style.SUCCESS(f'Created test: {TEST_TITLE}'))
        else:
            self.stdout.write(self.style.WARNING('Тест уже существует, пропуск.'))
            return

        for i, q in enumerate(QUESTIONS, start=1):
            Question.objects.create(
                test=test,
                type='single_choice',
                text=q['text'],
                options=copy.deepcopy(q['options']),
                order=i,
                weight=1,
                language='ru',
            )
        self.stdout.write(self.style.SUCCESS(f'Created {len(QUESTIONS)} questions.'))
